package inheritance;
class parent11{
	void selenium() {
		System.out.println("selenium");
	}
	void java() {
		System.out.println("java");
	}
	 void gk()
	{
		System.out.println("gk");
	}
}
public class Single_inherate {
	

	public static void main(String[] args) {
		
		parent11 p1 = new parent11();
		p1.selenium();
		p1.java();
		p1.gk();
	}

}
